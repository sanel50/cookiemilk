package com.java.class02;

public class VariableExample {
    public static void main(String[] args) {

        //Declaration of variable
        int num1;

        //Assignment of value into variable
        num1 = 50;


        //Declaration & Initialization of variable
        int num2 = 10;



        //Declaration & Initialization of multiple variables into single line
        int num3 = 30, num4, num5 = 90;
        num4 = 60;

        String str = "Hello World";
        String num6 = "20" , num7 = "40";


        System.out.println("num1");           //30
        System.out.println(num4 + num3);           //60
        System.out.println(str);           //90
        System.out.println(num6 + num7);           //90




    }
}
