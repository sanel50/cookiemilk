package com.java.class12;


//using for print 10 to 1


public class Print10to1 {

    public static void main(String[] args) {



        System.out.println("\t\tFirst Method");
        for (int i = 1; i <= 10; i++){
            System.out.println(11 - i);
        }


        System.out.println("\t\tSecond Method");
        for (int i = 10; i >= 1; i--){
            System.out.println(i);
        }





    }

}
